#include <stdio.h>
void main(){
    char op;
    int a,b;
    scanf("%d %d %c",&a,&b,&op);
    switch(op){
        case '+':
        printf("Thou art bequeth thine fletcher for thou art thine divine\n");//By Ignoble Immortal
        printf("Addition says Yoda=%d",a+b);
        break;
        case '-':
        printf("A CONFLICTING SITUATION\n");
        printf("Subtraction=%d",a-b);
        break;
    }
}
